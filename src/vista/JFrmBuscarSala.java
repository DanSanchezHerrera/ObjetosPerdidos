    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        int id_sala = Integer.parseInt(spnidsala.getValue().toString());
        Sala sa = new DaoSala().Buscar(id_sala);
        if (sa!= null) {
            JOptionPane.showMessageDialog(null, "Se encontro sala");
            spnId_sala.setValue(sa.getId_sala());
            txtDesc_sala.setText(sa.getDescripcion_sala());
        } else {
             JOptionPane.showMessageDialog(null, "No se encuentra sala");
        }
        


    }//GEN-LAST:event_btnBuscarActionPerformed

