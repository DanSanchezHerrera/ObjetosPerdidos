    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int pos_fila = jtblLista.getSelectedRow();
        String rut_encontrado = (jtblLista.getValueAt(pos_fila, 0).toString());
        try {
            DaoEncontrado dao = new DaoEncontrado();
            int respuesta = JOptionPane.showConfirmDialog(null, "¿Desea eliminar encontrado? "+rut_encontrado,
                "Eliminar Encontrado", JOptionPane.YES_NO_OPTION);
            if (respuesta == 0) {
                boolean resp = dao.Eliminar(rut_encontrado);
                if (resp) {
                    JOptionPane.showMessageDialog(null, "Eliminado");
                    limpiar();
                    Listar();
                } else {
                    JOptionPane.showMessageDialog(null, "No pudo eliminar");
                    limpiar();
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error eliminar");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

