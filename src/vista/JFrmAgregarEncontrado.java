    private void Listar() {
        DefaultTableModel modelo = new DefaultTableModel();
        jtblLista.setModel(modelo);
        modelo.addColumn("Rut");
        modelo.addColumn("Nombre");
        modelo.addColumn("Apellido");
        modelo.addColumn("Escuela");
        modelo.addColumn("Semestre");
        ArrayList <Encontrado> lista = new DaoEncontrado().Listar();
        for (Encontrado rec : lista) {
            Object [] fila = new Object[5];
            fila[0]=rec.getRut_encontrado();
            fila[1]=rec.getNombre_encontrado();
            fila[2]=rec.getApellido_encontrado();
            fila[3]=rec.getEscuela();
            fila[4]=rec.getSemestre();
            modelo.addRow(fila);
    }
    }

